package com.ycnet.frms.vo;

public class AreaMapBean {

	//经度  大
	private Double lng;
	
	//纬度 小
	private Double lag;
	
	private int seq;

	public Double getLng() {
		return lng;
	}

	public void setLng(Double lng) {
		this.lng = lng;
	}

	public Double getLag() {
		return lag;
	}

	public void setLag(Double lag) {
		this.lag = lag;
	}

	public int getSeq() {
		return seq;
	}

	public void setSeq(int seq) {
		this.seq = seq;
	}
	
}
