package com.ycnet.frms.bean;

public class SiteCode {
    private Long siteCodeId;

    private String devCode;

    private String sectCode;

    public Long getSiteCodeId() {
        return siteCodeId;
    }

    public void setSiteCodeId(Long siteCodeId) {
        this.siteCodeId = siteCodeId;
    }

    public String getDevCode() {
        return devCode;
    }

    public void setDevCode(String devCode) {
        this.devCode = devCode;
    }

    public String getSectCode() {
        return sectCode;
    }

    public void setSectCode(String sectCode) {
        this.sectCode = sectCode;
    }
}